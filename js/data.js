function getLinkGithub(link){
  var n = link.includes("https://duyit99.github.io/");
  var m = link.includes("https://duy99uit.github.io/");
  if(n == true)
  {
    let res = link.replace("https://duyit99.github.io/", "https://github.com/duyit99/");
    return res
  }
  if(m == true)
  {
    let res = link.replace("https://duy99uit.github.io/", "https://github.com/duy99uit/");
    return res
  }
  
}
